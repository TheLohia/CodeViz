# Sample code to parse and make network. Level 1 involves only a few functions to keep it simple

def add(a, b):
    return a + b


def subtract(a, b):
    return a - b


def multiply(a, b):
    return a * b


def divide(a, b):
    return a / b


def calculate(a, b):
    print("Sum: " + str(add(a, b)))
    print("Difference: " + str(subtract(a, b)))
    print("Product: " + str(multiply(a, b)))
    print("Ratio: " + str(divide(a, b)))
